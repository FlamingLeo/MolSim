#include "XYZWriter.h"
#include "utils/CLIUtils.h"
#include <filesystem>
#include <iomanip>
#include <sstream>

namespace outputWriter {

XYZWriter::XYZWriter() = default;
XYZWriter::XYZWriter(const std::string &basename) : m_basename{basename} {}
XYZWriter::XYZWriter(const std::string &basename, const std::string &dirname)
    : m_basename{basename}, m_dirname{dirname} {}

XYZWriter::~XYZWriter() { closeFile(); };

void XYZWriter::writeParticles(const ParticleContainer &particles, int iteration) {
    // create output directory in which to store generated XYZ output files
    if (!(std::filesystem::exists(m_dirname))) {
        if (!(std::filesystem::create_directory(m_dirname))) {
            CLIUtils::error("Error creating XYZ directory!");
        };
    }

    // define file name
    std::stringstream strstr, content;
    strstr << m_dirname << "/" << m_basename << "_" << std::setfill('0') << std::setw(4) << iteration << ".xyz";

    openFile(strstr.str());

    // write content to preliminary string stream
    content << particles.size() << std::endl;
    content << "Generated by MolSim. See "
               "http://openbabel.org/wiki/XYZ_(format) for "
               "file format doku."
            << std::endl;

    for (auto &p : particles) {
        std::array<double, 3> x = p.getX();
        content << "Ar ";
        content.setf(std::ios_base::showpoint);

        for (auto &xi : x) {
            content << xi << " ";
        }

        content << std::endl;
    }

    // finalize output
    writeFile(content.str());
}

} // namespace outputWriter
